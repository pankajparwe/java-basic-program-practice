package Finding_Numbers_From_list;

public class Even_odd_series {
    public static void main(String[] args) {
        for (int i=1; i<=100; i++)
        {
            if (i%2==0)
            {
                System.out.println(i+" number is even");
            }else{
                System.out.println(i+" number is odd");
            }

        }
    }
}
