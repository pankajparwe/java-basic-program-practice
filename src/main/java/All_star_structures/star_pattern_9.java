package All_star_structures;

public class star_pattern_9 {
    public static void main(String[] args) {
        for (int i=1;i<=4;i++)
        {
            for (int j=3;j>=i;j--)
            {
                System.out.print(" ");
            }
            System.out.println("*");
        }
    }
}
