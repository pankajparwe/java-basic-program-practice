package All_star_structures;

public class Star_pattern_pyramid_2 {
    public static void main(String[] args) {

    for(int i=1;i<=4;i++)
    {
        for(int j=4;j>=i;j--)
        {
            System.out.print("*");

        }
        System.out.println();
    }


    }}
